                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Distance learning can be defined in many different ways. Distance learning can be classes taken over the Internet, Web TV, cable TV, correspondence course, audio conferencing, CD-ROM, videotape or any other mechanism that allows a student to learn without traveling to a traditional classroom. These programs were first developed to assist people in remote or rural areas have access to the same education as people living in the major centers.<br><br>Today people of all ages and from every area of the country are involved in distance learning because it allows them to learn from home at he same time as they carry out their other responsibilities in life. Homemakers, veterans, even senior citizens are joining teenagers and young adults in these new and non-traditional learning programs.<br><br>There are is as much of a variety in distance learning programs as there are people who wish to advance their education. Most are associated with a regular school, college and university, and that allows them to offer accredited online degree programs. Others are completely separate companies that have been formed for the express purpose of providing distance learning programs.<br><br>You can take a course in the basics of plumbing from a technical institute or an advanced course in quantum physics. It really depends on your interest and aptitude to learn. Very, very few distance learning programs are free of charge, but some offered by community colleges are significantly lower in cost than the major colleges and institutions.<br><br>In distance learning like so many other things in life you pay for a brand name, like a prestigious university program, and you also get what you pay for you.<br><br>Some programs advertised through mass e-mails or by direct marketing are really very basic courses dressed up to look like more than they are. One quick way to determine the value of a distance learning program is to check if they have been approved and accredited by one of the national or regional accreditation agencies.<br><br>Distance learning is a great way to learn, but if your online degree is not accredited at the end of your studies then it may not be worth what you think it may be.<br><br>Look for the accreditation stamp of approval before you sign on the dotted line. Then take your time to review all of the options and find one that will not only get you an online degree but one that will open the doors of opportunity to you and your family.
