                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Through the wonders of science, infertile couples who were previously unable to bear children, due to reasons such as blocked fallopian tubes, low sperm count, low egg quantities or advanced age of the mother, are now able to conceive through in-vitro fertilization. The results are babies known as test-tube babies which are technically conceived outside the womb.
<br>
<br>In a natural scenario, the conception of a baby occurs when the ovum, travels from the ovaries, through the fallopian tube to be fertilized by the sperm of the father during sexual intercourse. From this point on, the fertilized egg will travel down towards the uterus and during the process division of cells will occur until it reaches its final destination at the wall of the uterus. However, in the cases of block fallopian tubes, the eggs are unable to travel from the ovaries to the uterus and conception cannot happen.
<br>
<br>Developed in the United Kingdom by Dr. Patrick Steptoe and Dr. Robert Edwards, the process of in-vitro fertilization involves removing eggs from the ovaries of the mother and combining them with the sperm of the father in a lab environment. The fertilized egg is then placed back into the uterus of the mother after 3 to 5 days and will remain there to grow till birth. Because of the low success pregnancy rate of this procedure, a few eggs are placed in the uterus to increase the odds of success. With this process, statistics have shown that the rates of multiple births have increased where 24 percent of the in-vitro fertilization births have produced twins. 
<br>
<br>The first test-tube baby, Louise Brown, who was born on the 25th July 1978, marked hope for other infertile couples to have a baby through this procedure. However, many others were concerned about the ethical issues surround this. One major area of concern was the fact that as the egg is located outside the womb for a few days while the cells multiplied before being put back inside the uterus, the health issues affecting this baby is unknown. Indeed, research has been shown that test-tube babies have a higher chance of birth defects and low birth weight, and researchers still have not been able to determine the reason for this.
<br>
<br>Other issue of concern is that through this procedure, some of the eggs fertilized in the lab are later discarded. Does this procedure mean that the researchers are actually killing potential people? How is the line drawn in this case? 
<br>
<br>Then there is also the issue of commercial opportunities for eggs and sperms to be bought and surrogate wombs to be rented with the purpose of creating babies. This process of embryo transfer to another mother, not necessarily the same woman who had provided the eggs, is known as placing the embryo in a gestational carrier. This procedure has been becoming increasingly common through technological advancement, which has made couples including women with uterus problems, have a chance in having their baby.
<br>
