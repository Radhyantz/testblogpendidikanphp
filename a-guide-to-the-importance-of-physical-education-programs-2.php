				<span style="font-size: 11pt">Physical activity offers a broad range of benefits, including the prevention of obesity, improved self confidence, and an overall sense of well-being. Physical education programs within the school setting can set the stage for how children view physical fitness, activity levels, and future health. Physical education programs also include general health and safety information in addition to providing opportunities for students to learn how to cooperate with one another in a team setting. <br><br>A Lifetime of Health<br>The school setting provides a structured atmosphere in which to incorporate physical health activities and ideally develop healthy habits for life. Studies indicate that promotion of a healthy lifestyle taught in physical education classes can influence long-term health benefits such as reduced rates of obesity, heart disease, high cholesterol, diabetes, and high blood pressure. <br><br>Perhaps just as importantly, physical education programs can teach students that physical activity can be fun. With a broad range of games and activities, children are exposed to forms of exercise that don't simply involve running around a track. Games and other activities incorporate teamwork, strategy, skill-building exercises, and fun.<br><br>Nutritional Information<br>Physical education classes are ideal for introducing basic nutritional concepts to children. Poor eating habits are common among many children and adolescents; however, a solid foundation in healthy eating choices can help lay the groundwork for improved food choices. Children who eat regular, healthy meals consisting of a wide range of food choices concentrate better in school and are less disruptive. Healthy eating also decreases the chances of children developing serious health problems early in life and reduces obesity rates among youth and into adulthood.<br><br>Life Skills<br>Physical education also provides an opportunity for children to develop critical life skills, such as problem solving, strategy, and working together. Many team sports require participants to work together to achieve a goal. Children also learn the basics of good sportsmanship and that there is much more to sports and physical activities than simply winning or losing. Sports require training, mental and physical preparation, and help build self-confidence. <br><br>Mental Health<br>Regular physical activity has shown to have many psychological and mental benefits in addition to the physical ones. For example, regular exercise can reduce feelings of depression and anxiety and promote an overall sense of well-being. The increased blood flow during exercise transports oxygen to all parts of the body, including the brain, which can help improve memory and reasoning skills. Conversely, a lack of oxygen, which can result from not enough deep breathing, can lead to disorientation, confusion, fatigue, and memory and concentration difficulties.
