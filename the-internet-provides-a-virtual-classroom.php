                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Do you have a passion for a certain subject?  Maybe you want to know more about elephants or the computer code to create computer games.  You can find information about any of your interests online.  The Internet not only provides basic information on any subject, but also lesson plans, activities, and real world examples.  It is a privilege and adventure to have such robust information at your fingertips.<br><br>The Internet is a virtual classroom.  You can read about any topic you desire.  You can take online tests and quizzes to track your progress.  You can access free worksheets and lesson plans.  The amount of information can be overwhelming.  Let's take a look at the advantages of different virtual classroom resources.<br><br>Online Reference Resources:<br>Reference resources include information from encyclopedias, dictionaries, and any scholarly journal or publication.  The advantage of this information is that it is credible, well respected academic information.  It is like having the entire library in your computer.  Instead of flipping page and page trying to find your information in heavy, dusty books, you can easily search the electronic versions with keywords.<br><br>Online Activities:<br>Online activities can extend learning outside of the traditional classroom.  You can check out websites for museums and places all over the world.  Need information about England for your next geography report?  You can search for British history in the reference resources and then experience England's attractions through online travel adventures.  You can view pictures of Britain's landmarks and attractions.<br><br>Online activities in math and science involve puzzles and science projects.  Have a blast solving logic or number puzzles.  Learn more about science by checking out online science experiments.  Many websites have step-by-step instructions for science experiments using items you can find in your own home.  Become a mad scientist in no time flat.<br><br>Online Tutors:  <br>Sometimes, no matter how much information you read, you still don't understand a topic.  For example, you have read about how to solve a long Algebra problem, but keep getting stuck on one step in the problem.  You need one-on-one help with a live person.  You're in luck.  Many homework help websites offer live tutors.  You can connect through a website "whiteboard" and ask them your questions in real time.  You can also submit questions to forums and have a tutor e-mail you an answer.<br><br>The Internet provides a virtual classroom that you can access from your own home, school, or library.  The virtual classroom never sleeps, so you can find an answer to your question any time day or night.  The Internet classroom is waiting.  Are you ready to be its student?
