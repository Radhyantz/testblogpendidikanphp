				<font size="3">A short biography on some of Europe's most loved and hated Monarchs -  Pt 1 Vlad Tepes (Dracula)</font></h1></font></strong> 
					<p align="left"> 
					<font face="Arial" color="#000044"><font size="2">By</font><strong style="font-weight: 400"><font size="2"> 
					Stuart Bazga</font></strong></font><br>
                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Vlad Tepes or Dracula was born in 1431, in the fortress of Sighisoara, Romania. His father was the military governor of Transylvania and a member of the Order of the Dragon. The order was created in 1387 by the Holy Roman Emperor and his second wife, Barbara Cilli.
<br>
<br>In the winter of 1436-1437, Vlad(Dracul) became prince of Wallachia and took up residence at the palace of Tirgoviste, the princely capital. In 1442, he and his younger brother Radu were taken hostage by the Turkish Sultan Murad II. Dracul was held in Turkey until 1448, while his brother Radu decided to stay there until 1462.
<br>
<br>At 17 years old, Vlad, supported by troops lent to him by pasha Mustafa Hassan, tried to seize the Wallachian throne but was defeated by Vladislav II (who had earlier assassinated his father and oldest brother ) after two months or armed conflict. Vlad had to wait until 1456, when he was able to seek retribution against his father's assassin.
<br>
<br>Vlad's first act of vengeance was aimed at the boyars of Tirgoviste for the killing of his father and older brother Mircea. Around Easter of 1459, Vlad had all the boyar families arrested and impaled the elder members on stakes while forcing the others to march from the capital to the town of Poenari. He then ordered them to build him a fortress on the ruins of an older outpost overlooking the Arges River. Many nobles died in the construction of this castle, the ruins of which can still be seen today.
<br>
<br>Vlad became known for his brutal punishment techniques; often ordering people to be skinned, decapitated, blinded, roasted, hacked, buried alive, stabbed and blinded to name a few. He also liked to cut off his victim noses, ears and sexual organs. But his favourite form of torture was impalement on stakes, hence the surname "Tepes" which means "The Impaler" in the Romanian language. It was this form of punishment that he used against Transylvanian merchants who ignored his trade laws.
<br>
<br>There are many tales about the psyche of Vlad Tepes. He was known throughout the country for his fierce adherence to honesty and order. Almost any crime, from lying and stealing to killing, could be punished by impalement. Being so confident in the efficiency of his law, Dracula placed a golden cup on display in the central square of Tirgoviste. The cup could be used by thirsty travellers, but had to remain on the square. It was never stolen and remained entirely untouched throughout Vlad's reign. He looked upon the poor, vagrants and beggars as thieves. Consequently, he invited all the poor and sick of Wallachia to his court in Tirgoviste for a magnificent feast. After his guests had eaten and drunk their fill, Dracula ordered the hall boarded up and set on fire. There were no survivors.
<br>
<br>At the beginning of 1462, Vlad launched a campaign against the Turks along the Danube River which was very successful, managing several victories. In retaliation for these losses, the Sultan decided to launch a full-scale invasion of Wallachia with an army three times larger than Dracula's. Vlad was forced to withdraw towards Tirgoviste, burning villages and poisoning wells along the way.
<br>
<br>These acts were designed to hinder the Turkish army in their search for food and water. When the Sultan’s armies finally reached the capital city, exhausted and hungry, they were confronted by a horrific sight: thousands of stakes held the bodies of some 20,000 Turkish captives, which came to be known as "Forest of the Impaled." The scene which was laid out before them had an immediate effect; the Sultan hungry and worn out retreated. The Sultan Mehmed left the next phase of the battle to Vlad's younger brother Radu who pursued his brother and wife to Poenari castle on the Arges River.
<br>
<br>Dracula's wife, in order to escape Turkish capture, committed suicide by hurling herself from the upper walls, her body falling down the cliff face into the river below. Vlad managed to escape the siege and made his way to hungry with the help of local peasants. Upon his arrival the Hungarian king Matthias arrested Dracula and imprisoned him at the Hungarian capital of Visegrad.
<br>
<br>In 1475, Vlad Tepes again became prince of Wallachia where he enjoyed a very short third reign. He was assassinated towards the end of 1476.
