o to the infoplease Homework Center at http://www.infoplease.com/homework/.  You can search for information about Albert Einstein in their reference resources.  Their reference resources include Almanacs, Atlases, Encyclopedias, and Biographies.  The search on Albert Einstein will bring about 120 resources.  A benefit of using these resources is that they are resources you would use in the library.  Therefore, they are appropriate to use for academic material.<br><br>There is a vast amount of information available to you on the Internet.  Take advantage of the homework websites as often as you can.  The main thing is that you find credible information and not rely on homemade fan sites or websites.  Use scholarly journals, encyclopedias, and other respected reference materials when doing your research.  Your teacher will be impressed with your researching skills.
