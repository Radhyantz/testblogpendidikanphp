				<span style="font-size: 11pt">When it comes , there are a number of considerations. Probably the most common, is the fact that, as a parent you just are uncertain as to how to respond to the gifted child’s unique development and personality traits. This also entails a difficulty in your expectations of the child. Also, one of the unique difficulties in parenting a gifted child stems from the fact that many times, although the child is gifted with advanced cognitive abilities, is still quite on level par with other children of their age in the areas of emotional and physical development. Greater challenges can even arise if you have a gifted child in a family with more than one child. But aside, from all the challenges, parenting a gifted child can be extremely rewarding and joyful because it is truly a very unique experience.<br> <br>As any parent would ask… How can I best be helpful for my gifted child? Well, first and foremost, do what you do best as a parent and love and care for your child’s needs regardless of their specially abilities. Although many of your gifted child’s characteristics may be at an advance level when compared to their age group, the basis of parenting really isn’t that much different.<br><br>Keep available to your gifted child many areas of opportunity to learn and be challenged.<br><br>Encourage your child to remain engaged in a broad range of activities and skills beyond those which they are naturally gifted.<br><br>Be supportive<br><br>As your child gets older, speak with them about their talents and some of the challenges that they face and why they are special.<br><br>Constantly teach and fine tune their social skills so they remain comfortable socially.<br><br>As a proud parent... don’t push or show off your child.<br><br>Don’t expect your child to be ‘gifted and talented in everything.<br><br>Above all... don’t forget that your gifted child is still very much a child. Still needing all the love, caring, and support that any child needs and thrives on.
