				<span style="font-size: 11pt">It's  time.... Do you have everything you need to start the year off right? Confidence is key to success!<br><br>For those first time jitters, help welcome your pre-school and kinder students with a walk to the door and a quick goodbye. As you give your child a firm hug and kisses, remind them to have a fun day and that you will be back to pick them up before they know it. This will leave them with the confidence they need to know they are loved and safe.<br>Quick good-byes are always best even though they can be painful to let go of your little one. Rest assured your little one is going to be well cared for throughout the day. Your confidence in your child's school and teacher will help give your child the confidence he/she needs to say goodbye easier. Remember children learn what they see. So if you are nervous and hesitant to let go, your child will be too. <br><br>For older children, going back to school is usually filled with the wonder and excitement of sharing summer stories and seeing who is in their class this year. Elementary students through high school students usually greet the new school year with anticipation of all the new things they will get, new clothes, new books, new back packs and most importantly making new friends. While making new friends can be exciting it is not always easy for all children to warm up to new people especially if they are in a new school. Your encouragement and support will go a long way in reminding them that they are likeable and they have a lot of good qualities that will make others want to know them. If your shy child finds going to school the worst nightmare in the world, try and discuss this with your child's teacher ahead of time and come up with ways to smooth the transition back to school.<br><br>Remember, all children have fears and anxieties about the first days or months of school, your support is needed now to guide them to succeed with confidence.<br><br>Have a safe and fun school year!<br><br>
