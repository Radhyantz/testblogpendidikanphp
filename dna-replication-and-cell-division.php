				<span style="font-size: 11pt"><p>DNA replication basically means creating two entire DNA strands for each daughter cell in place of the single parent cell. Prior to the cell division, its DNA within the nucleus is released by an enzyme to break the hydrogen bonds between the bases, resulting in two halves of the DNA. The uninhibited nucleotides within the nucleus bonds with the bases of both strands. Bonds with T alone while C with G, leading to exact matches for both strands.</p>
<p>There are four phases in cell division, or mitosis (normal cell division, meiosis forms sex cells).</p>
<p>Prophase: Pairing of chromosomes occurs and following replication, the parent cell has two complete sets. Two poles are formed as the nucleus disappears.</p>
<p>Metaphase: Alignment of the chromosomes is at the equator between the two poles.</p>
<p>Anaphase: The chromosomes split up towards each pole. Cell membrane division begins.</p>
<p>Telophase: Appearance of nuclei is at each pole and membrane divides. Two cells possess identical chromosomes.</p>
<p><b>DNA Replication</b><br>
The DNA composition where two separate strands store all genetic information, replicate easily. A DNA molecule has a polymer opposite of the other, like a photonegative. One side enables the other to be recreated. This duality simplifies copying a DNA molecule remarkably.</p>
<p>Replication causes synthesization of DNA molecules. First a helicase enzyme unwinds the double helix, at any point on the strand, progressing to two replication forks by enzymes extending the helix release in both directions. With unwinding and straightening out of the DNA, DNA polymerase, another enzyme functions. It matches the exposed nitrogenous bases with new nucleotides from the surrounding nuclear fluid. The nucleotides and separated polymers fuse as per the regular Watson-Crick pairing rules. The completion of the separation and re-matching of the DNA molecule makes two perfect copies.</p>
<p>DNA replication remains partially unknown. Signals that start the process, the reason some cells don’t replicate and the cause of uncontrollable replication of cancerous cells remain unexplained.</p>
<p><b>Replication in Prokaryotes</b><br>
Prokaryotes or bacteria store almost the entire genetic information of an organism in a single large circular DNA ring and not in multiple chromosomes of DNA strings with unconnected ends. This replication is almost identical to eukaryotic DNA except in the use of only two replication forks. Eukaryotes and prokaryotes differ, with multiple replication sites active simultaneously in the former and the speed of the latter requiring only two, one for each direction The rate of replicating bacterial DNA is almost one million base pairs per minute whereas the average is 500 to 5000 pairs for other organisms.</p>
<p><b>DNA Replication is Semi-Conservative</b><br>
On completion of the replication process, two DNA molecules result, which are identical both to each other as well as to the original. There is not the slightest alteration in each strand of the original molecule due to its role as the template for the synthesis of a complementary strand. This replication method has been termed semi-conservative; as one half of each new DNA molecule is old while the other half is new.</p>
<p><b>Finally…</b><br>
The need for DNA replication or cell genome duplication is created by each cell division. As with all cellular activities, specialized proteins are needed for the purpose of replication. <a href="http://www.biology-online.org/">DNA replication</a>  is surprising as a process in all organisms except in the case of humans where comprehension can prove very complex.</p>
