                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">After a full week of training, you are still a little nervous about your new job.  All of the information you need to digest, the new environment you are adjusting to, and the new faces you will be getting acquainted with is just a bit overwhelming.  <br><br>Meeting the expectations of a new job and being the new face among an established network of co-workers is an uncomfortable position for everyone.  But you can find comfort that new hires everywhere face the same challenges.  <br><br>All of us at one time or another has had those feelings of disconnection.  Yet, if co-workers and managers take a responsibility by continually coaching and preparing new people for their jobs, they, as well as the novice, will benefit from the experience.  This will ultimately benefit the company as a whole.  <br><br>For The New Hires-  Attaining new skills, learning the ropes of the job, and establishing him or herself in a new community, with the help of an experienced co-worker gives them valuable insight and knowledge about the job and community.  Co-workers are their most accessible resources on the job.  Veteran employees know what works best with their field of work. <br> <br>For The Co-Worker- Because veteran employees have developed job knowledge and skills and are easily accessible, they are the most logical choice to coach, mentor and train a new employee.  This opportunity offers the veteran employee a chance to develop their leadership skills.  It also boosts self-confidence that others value their experience and dependability.  With the duty to coach the new hires, a veteran employee takes stewardship for the new hires seeing that they learn all the aspects of the job.  With manager coaching, this leadership can be developed further, giving them a chance for promotion later.   <br><br>For The Manager- The manager is often overloaded with too many other responsibilities.  Coaching employees through his or her veteran workers, helps maintain continuity of work protocol and efficiency.  The new employee isn’t waiting for the manager to get time to explain more procedures.  When his or her subordinates become proactive participants in the coaching of new hires, the manager gains more time to turn his or her attention to other employees as well.  By coaching the veteran worker in training new employees, the manager begins to develop the potential of the veteran worker.  <br><br>The relationship between the new hire and his or her co-workers and managers will become a valued partnership in the success of a company.  Briefly, coaching employees guides new hires, develops leadership among employees, and provides a collaborative effort in the success of the company.
