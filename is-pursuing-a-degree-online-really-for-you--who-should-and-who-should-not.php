				<span style="font-size: 11pt">Stanford University’s Online Lessons Learned <br><br>Stanford University’s Center for Professional Development has recently completed a survey of those that have been successful in completing online degree programs globally.  The results of their research provide useful guidance for anyone considering an online program.  The findings of their research are summarized here:<br> <br>Best for motivated, self-directed students who have a definite goal.  The Stanford survey confirms that the best students are those that don’t need constant attention and guidance, and most importantly, have a clear goal for their educational degree.  <br><br>Online learning attracts students who would not otherwise have taken courses.  The survey shows that it is definitely an either-or situation for many students – many don’t take classes at all unless they are offered online. <br><br>Need for recognition and achievement.  Stanford has found that the most effective students for online programs like to compete for high marks online and eventually earn a degree that is delivered to them anywhere in the world.<br><br>High need for autonomy.  Stanford’s results show that any student who prizes their autonomy over being overly socially involved excels at online learning programs.  <br><br>Ability to commit up to 10 hours a week to the course.  Stanford’s study show that the best students don’t necessarily put in the longest hours; they are the ones that make the most of the hours they commit.  The minimum amount of time Stanford found students could spend per week on entry-level courses was 4 hours per week; for graduate-level courses, 15 hours or more.<br><br>Confidence in sharing their own experiences.  Many of the respondents to the Stanford said that the best classes were marked by other students sharing their own experiences.  The willingness to share experiences makes for the best online classes, and this is substantiated by the Stanford study.<br><br>Like to write.  Stanford found this as a definite requirement, but critical to the success of anyone in an online program.  Writing is the primary means by which student’s interaction with each other and their instructors is achieved, so this is also important to keep in mind.<br><br>Bottom Line: The Stanford survey shows that the more independent and focused a student is, the more successful they will be in distance learning programs.  Writing is also important, as is the willingness to contribute to classes. <br><br>
