				<span style="font-size: 11pt">Here are some exact examples of what you can do to set a <br>song together. <br><br><br><br>You  can get inspired by a number of methods <br>such as: <br><br>1. A chord progression<br>2. A cool riff<br>3. A lead melody<br><br><br><br>One of the things you require to do is generate a part that <br>compliments your original idea such as adding a melody to <br>a chord progression or adding up a chord progression to a <br>melody. <br><br><br><br>Say you have a chord progression you truly like. <br><br><br><br>To make your musical painting, you have to know theoretically <br>what key you're in. If you're weak on modal theory, but <br>familiar with a scale or neck pattern that sounds fine against your <br>chord progression, employ what you know to create a melody over <br>it. <br><br><br><br>The easiest way to accomplish this is to find a multitrack recorder and <br>record your chord progression on one track. Play it over and <br>over a lot of times. <br><br><br><br>When you play it back, work out your melody on another track. <br>As you're working on a melody line, try to take note for cool <br>harmony among your melody and chord progression. If only <br>one exacting note sounds totally great against one of the <br>chords, go with it and start building around that great <br>relationship. <br><br><br><br>Now, if you're notes are all correct but you can't seem to <br>come up with something you like, it may be time to inject a <br>new technique to spice things up a bit. <br><br><br><br>Let's say you recognize the chords and you know the notes. Try <br>looking at the notes in a whole new way such as: <br><br><br>1. Arpeggios. (The notes of the chords) <br><br>2. Skipping strings for wider note intervals. <br><br>3. Right hand tapping. <br><br>4. Whammy bar effects. <br><br>5. Pinching harmonics...<br><br><br><br>One of the biggest problems is not being able to decide for <br>if something was good or not. If you have this trouble <br>too, get someone else's opinion. If possible someone who can <br>suggest helpful insight. <br><br><br><br>If you can't find someone to critique your tune, you MUST <br>decide for yourself and move on...otherwise you'll never get <br>your tune finished. <br>
