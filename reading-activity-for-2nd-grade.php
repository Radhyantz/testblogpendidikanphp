                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">As you think about or look at a reading activity for 2nd grade keep in mind that about the 2nd grade reading level is an area of reading transition for the young reader. As you begin to make the transition into a 2nd grade reading level with your child, its important to keep notes for yourself at to what arrears need improvement or more work and where you will start your activities the next time in your overall reading activities for your second grader. With 2nd graders, your reading activities that you have planned have to be fairly flexible. Again, this is a transition time for kids this age. Their reading material is going from shorter to longer sentences; of course the degree of difficulty rises… And the dreaded pages with none or not many pictures are becoming way too frequent! Indeed this transition time can cause bad days at the old reading shop. And when this happens you won’t get accomplished what you wanted to that day and tomorrow you’ll have to spend time going back for review and practice. And you’ll have to adjust plans as needed. Many times it’s just a matter fine tuning the allotted time you have planned for each reading activity.<br> <br>Don’t let these minor difficulties set you back. Many kids as they enter the 2nd grade reading level have these occasional difficulties. It is important for you to understand that this does occur and isn’t at all uncommon at the second grade reading level. Don’t panic. Be cognizant of your child’s attention. Take breaks if you begin too notice rising frustration levels or read something funny or fun that the child likes.<br> <br>Do a bit of research on some reading activities for the second grade in order to determine some baselines areas such as how many words per minute should you be looking for and be sure to track the progress. What degree of difficulty are the words and the sentence structures they form. Once you’ve books that you are comfortable that they are at the right reading level, then it’s just a matter of practice, and a plan of continuous improvement.<br> <br>Remember the second grade reading level is an age of reading transition essentially making the step from bigger letters, shorter words, less emphasis on the phonics, to fewer pictures, more words, vocabulary building, and longer sentences. It is at the second grade reading level that you should begin monitor and track the areas of reading fluency and see a steady progress in the words per minute your child can read and comprehend.
