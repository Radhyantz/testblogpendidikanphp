				<span style="font-size: 11pt">Do you have trouble with math?  Do you feel anxious when taking math tests?  Do you want to improve your math skills, but don't know where to do this?  If any of these problems plague you then look to the Internet for extra help.  There are several websites that provide information, lessons, tutors, and even games.<br><br>Let's start with basic online math games.  Check out Aplusmath.com at http://www.aplusmath.com/games/.  This website provides games that will test and help you practice your basic math skills such as addition, subtraction, multiplication, and division.  You can play math bingo and hidden picture games.  This is a great way to brush up on your basic arithmetic skills.  Aplusmath.com also provides flashcards, worksheets, and a homework helper.  Definitely try the homework helper.  It allows you to input a math problem and helps you find the answer.<br><br>Once your basic math skills are mastered try your hand at critical thinking math games.  A great website is Math.com.  Math.com at http://www.math.com/ can be your one stop shop for all your math needs.  Math.com provides homework help, practice, tutors, calculators & tools, and math games.  The last word, "games" probably peeked your interest.  Yes, you can improve your math skills through games.  Learning multiplication and logic skills doesn't always have to involve a textbook and a classroom.<br><br>You can work on your logic and planning skills when playing the game "Fiver".  This game consists of a grid containing all white pieces.  The goal is to change all the white pieces to black pieces.  That sounds simple enough, but how does it improve my math skills?  There are conditions that are imposed on you each time you change a white piece to a black piece.  These conditions affect the color of the surrounding pieces.  This is similar to the conditions or rules in math.  <br><br>For example, when working with positive and negative numbers you have to follow specific rules when you add, subtract, multiply, and divide them.  However, the puzzle takes it one step further in that you not only have to follow the rules, but plan ahead.  Planning ahead and using logic is an example of higher level thinking.  These critical thinking skills will help you in math, science, and business.<br><br>These are just two of hundreds of math websites to help get you started.  Playing a math game is a learning experience.  Math games supply the extra help needed for basic skills and extra challenge that will help you to improve your math skills.
