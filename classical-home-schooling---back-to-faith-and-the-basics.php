				<span style="font-size: 11pt"><br>        <br>Home schooling material can range from following the public school system standards to classical home schooling.  What is a classical education and how does it fit into home schooling?<br>        <br><br>Classical education involves the art of speaking, communicating, and writing.  It was the dominant form of education through the 19th century with academic learning in literature, history, art, math, and science.  Latin and Greek languages were taught as a basis of all subjects.  Students were trained through lectures, debates, and discussion.  Critical thinking, logic, and independent reasoning were stressed.<br>        <br><br>Some of these features of classical education are still prevalent in today’s classroom; however they are being overshadowed by standardized testing.  Teacher’s are encouraged to “teach to the test” in order to adhere to government mandated requirements.  This type of learning is rigid and stifles the student’s learning experience.<br><br><br>This constraint on learning, along with the separation of Church and State in the public school system, has driven many families to provide classical home schooling for their children.  Classical home schooling supporters believe that textbooks and teaching in the public school system have been over simplified.  Latin, once a common language offered in public school, is now replaced by Spanish, German, and French.<br>        <br><br>Specifically, classical home schooling consists of a rigorous academic structure including:  Grammar, Dialectic, and Rhetoric.  Grammar refers to language, Dialectic refers to reasoning, and Rhetoric refers to logic.  They are taught based on a student’s age.<br>        <br><br>Pressure has been placed on public schools to be politically correct.  This has caused religion to be removed from the public school systems over the past century.  However, many families believe that Faith-based education is beneficial.  Classical home schooling comes from a Christian view point.  Study of the bible and the influences of Christianity in education play an important role in classical education.<br>        <br><br>Need more information on classical home schooling?  There are several resources available on the web.  You can subscribe to the Classical Home Schooling Magazine at classicalhomeschooling.com/.
