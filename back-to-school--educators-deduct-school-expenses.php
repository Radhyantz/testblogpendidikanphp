				<span style="font-size: 11pt">As teachers and students head back to school following a glorious summer, it’s time to remind teachers to organize 2004 school expenses. Under a temporary tax code change, teachers can deduct certain school-related expenses from adjusted gross income.<br><br>Educator Expense Deduction<br><br>If you work in the education field, you may be able to deduct up to $250 from your adjusted gross income for 2004 taxes. Unfortunately, the deduction is only applicable to 2004, but there is a reasonable possibility it will be extended to the 2005 tax year and beyond. As a result, you should continue to keep records so you can claim the deduction if it is extended. So, who can claim it and what can be claimed?<br><br>“Educators”<br><br>Under the tax code provision, “educators” are defined as a fairly broad group of professionals. You are an education if you comply with the following guidelines:<br><br>1. You teach kids in kindergarten or through grade 12; <br><br>2. You are a teacher <br><br>3. You are an instructor <br><br>4. You are a counselor <br><br>5. You are an aide, or<br><br>6. You are a principal<br><br>If you fit within one of the above positions, there is an additional time requirement that must be met. You must work at least 900 hours in an elementary or high school during the year in question. This equates to roughly half a year. <br><br>Expenses<br><br>As an educator, you are allowed to deduct unreimbursed expenses you paid for school room items. Examples include books, computer programs, writing supplies and those little stars I used to love getting on my book reports. Just make sure the school is not covering the costs. <br><br>The educator expense deduction is a rather disappointing $250, but every deduction counts when it comes to taxes. Make sure you claim the deduction and keep your receipts for the write off. <br>
