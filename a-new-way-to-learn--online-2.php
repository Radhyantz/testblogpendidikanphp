                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Many of the most universities in the country are realizing that no everyone can attend regular classes to obtain a college education. As a result almost all of them offer distance learning programs that allow students from anywhere in the country to get a university education and even get an online degree.<br><br>There are many barriers to overcome in order to get a college education. They range from cost to motivation to accessibility. Many young people and their families simply cannot afford to pay the tens of thousands of dollars that is necessary in a regular degree program. Others live in remote areas of the country that make it impossible to attend college without major support and subsidies.<br><br>Distance learning allows them to overcome many of these challenges by connecting to the school of their choice through the Internet and enrolling in online degree granting programs in their field of choice. They can get a college education and still help out on the farm at home or they can work to support themselves during the day and take online classes at night.<br><br>These types of programs have transformed the higher education landscape almost overnight. Many millions of American kids will follow their college dreams right after high school, but they are now being joined in the academic world by military veterans, housewives, and thousands of other people who want a second chance at living out their dream.<br><br>Distance learning has democratized the higher education system in this country and it doesn’t matter now how much money your family has or what stage of life you choose to continue your education. An online degree from a reputable and accredited program is worth just as much as if you went to that school for four years right after high school.<br><br>If you could picture an online classroom you would see a wide range of ages and life experiences sitting at their home computer, engaged in the pursuit of an online degree. Their enthusiasm to learn is just as great as those who sit in a regular classroom or lecture hall at the university. But what may strike you the most is that these people who are studying for an online degree have a zeal for learning and a motivation to get ahead that any university professor would love to see in his much younger class. These people desperately want to get their college education and distance learning will help them get there.
