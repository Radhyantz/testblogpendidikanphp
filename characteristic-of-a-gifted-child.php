                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Since it is widely agreed upon in the educational and child development communities that early recognition of the characteristics of a gifted child, is a key factory in successfully developing the child, it is important for the parent to have at least a general understanding of these gifted characteristics.<br> <br>First of all in order to be classified as a gifted child, the child must have the ability to perform at a level that is significantly beyond other children of like age. O.K. So, for a parent, what might this look like or what should I look for in my child?<br> <br>The following are some guidelines to help your awareness when it comes to recognizing the characteristics of a gifted child. Keep in mind that there are varying levels of giftedness, as you view the guidelines.<br> <br>Cognitive Skills (thinking or thought processing):<br><br>Ability to master new skills with extraordinary speed.<br><br>A deeper knowledge than other children the same age. <br><br>Extraordinary memory and recall of events, facts, and/or figures. <br><br>Advanced creativity and ability to improvise. <br><br>Acute alertness to their surroundings. <br>Advanced or more complex sense of humor.<br> <br>Learning Patterns:<br><br>Great and constant curiosity along with high level of motivation to learn.<br><br>As a result of the previous, being bored easily if not challenged.<br><br>Advanced ability to stay focused or lengthy attention span. Even with more difficult topics. <br><br>Advance organizational or planning skills. <br><br>Advance logic and problem solving skills.<br> <br>Speech and Language:<br><br>Well developed word vocabulary and language compared to children of the same age. <br><br>Creative uses of words and sentence structure and again, sense of humor. <br><br>Ability to adapt their use of the language to a given level to fit the situation. <br><br>Ability to understand and carry out complex instructions. <br><br>General advanced ability in reading, writing, and working with numbers.<br> <br>Social:<br><br>Sensitivity to other’s feelings.<br><br>Use of their advance language skills to resolve conflict or debate. <br><br>Organize and direct behavior of other children (may seem bossy to other children). <br><br>Many times will feel more ‘at home’ with older children. <br><br>May have high expectations of themselves (and others) that can lead to frustration and even anger.<br> <br>Other:<br><br>A generally high aptitude for logical problems, games, puzzles, or any type of problem solving or activity that requires creative thinking and reaction.<br> <br>Final Note: Although these characteristics of a gifted child may provide you with some initial insight and understanding, it is important to seek a formal assessment of your child in this area if you feel that your child exhibits several of these characteristics.
