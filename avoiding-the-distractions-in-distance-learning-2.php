                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">In order to learn at home in a distance learning program we first must learn to avoid the distractions. They are the things that will keep us focused and motivated on learning and studying and if we don’t find a way to avoid or eliminate them we can find ourselves not accomplishing what we set out to do.<br><br>The first step to reducing distractions to our distance learning program is to identify them. They may include everything from our family and friends, although we certainly don’t want to eliminate them, to noise from both inside and outside our home. They may also include the constant intrusion of the telephone or our cell phone to the master distracter, the television.<br><br>Once we have identified the sources of our distractions we can then develop a plan to deal with them. Like most things that need to change we need to go first. Start by developing a study plan and a timetable of hours that you will devote to your distance learning program. Next determine which space in your home would be the best place to your studies and organize it so that it is comfortable, and hopefully separate from the rest of your household activities.<br><br>Then talk to your friends and family and advice them of your plan and timetable. Ask them for their cooperation during this period of time and to respect your need for time, space, and quiet to do your work. You may need to remind your kids that they need to keep their music a little lower from time to time and to tell your friends to drop over for coffee at another time, after your studies are complete.<br><br>The next step is to reduce the noise level so that you can concentrate on your studies. Find a quiet place in your home so that you are not disturbed by the regular noise of your kids playing, the neighbors mowing the lawn, or people having heated discussions or debates that sometimes grow loud with their arguments or laughter. Use headphones if you have to, but create that quiet space that will help you learn.<br><br>Finally, turn off the outside world. Shut off your e-mail program and turn off your personal digital assistance and pager. Unplug the telephone to avoid interruptions. Last but not least, turn off the television, the master distracter. You can tape the latest episode of your favorite television show and watch it later, but don’t try and do both. One will suffer and it almost always be your distance learning program.<br><br>Stay focused, stay motivated, reduce and eliminate distractions, and study hard. You will have your online college degree in less time than you think.
