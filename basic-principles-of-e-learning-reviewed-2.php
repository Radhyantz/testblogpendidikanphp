                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">The term "e-learning" refers to a very broad range of educational opportunities within the electronic world, from "live" chat classrooms online to self-paced study through a website or CD Rom, to courses delivered via email. The various delivery methods serve different purposes for the learner, and learners may do better using a particular method over another. For example, someone who has a high level of self-discipline, coupled with a strong desire to learn may do well in a self-paced study program. Other learners may do better with live interaction through chat groups, message boards, or regular instructor communication to maintain motivation and provide a "real world" feel to e-learning.<br><br>With the advancement of technology and increasing availability of high-speed Internet access, it is now possible to earn a degree entirely online or for businesses to provide company-wide training through e-learning.<br><br>Is it Interactive?<br>Because e-learning often takes place alone rather than in a classroom, learners will do best if there is some type of interaction. Not only does this make the instruction more interesting, it also promotes learning. Interaction can occur many ways, such as through scheduled chats or interactive lessons. If participants are asked to simply read or listen and regurgitate, the learning experience will not be as successful. Furthermore, because e-learning provides unique interactive experiences, quality programs will take advantage of as many of these opportunities as possible.<br><br>Does it Provide Feedback?<br>Learners need to know how they are progressing, and regular feedback informs participants on both their areas of weakness and areas of strength. This allows participants to focus on those areas that need improvement or practice. Again, the delivery method of feedback can vary greatly, from self-testing at regular intervals or direct feedback from an instructor. The key is that feedback needs to be ongoing and consistent. Limiting feedback to a final test or review does not provide the learner with opportunities to individualize learning throughout the program. <br><br>Is it Learner-Based?<br>The very nature of e-learning is that it is flexible and is geared toward the needs of the learner--when it is done well, that is. E-learning opportunities need to be flexible in that the learner can access and use the technology at his or her convenience. While interaction with the instructor or other participants may need to be scheduled, the majority of the learning experience should be available at any time.<br><br>The technology used also needs to be appropriate for the targeted audience and not too difficult to use. If learners are spending more time trying to learn the delivery method rather that the subject, it is not time well spent. If possible, find a program that allows users to review a demonstration of the delivery prior to signing up for the course.
