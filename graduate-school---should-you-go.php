				<font size="3">Graduate School:  Should you go?</font></h1></font></strong> 
					<p align="left"> 
					<font face="Arial" color="#000044"><font size="2">By</font><strong style="font-weight: 400"><font size="2"> 
					Stuart Simpson</font></strong></font><br>
                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">You don’t need to go to graduate school.  This is true.  You’ve got your college degree and this will help you maintain a roomy house in a nice neighborhood.  But what will a graduate degree do for you?<br><br>First, there is a little more money.  Lifetime earnings average $234,000 more than an individual with just a bachelor’s degree.  But if you got a doctorate, then increase the amount by $504,000 above the master’s level.  Doctoral education along with the apprenticeship work can take 10 years or longer.  And the 10 years are some of the most grueling work you’ll ever do.  So let’s focus on just the graduate degree.  Whew.<br><br>Another reason for obtaining a graduate degree is to make yourself more valuable to your employer.  They aren’t going to can the guy with the MBA.  In this age of layoffs and cutbacks, you need a competitive advantage over your coworker.  Everyone is replaceable.  If you think you aren’t, better think twice.  Just make it harder on your employer to find another employee with a graduate degree at your salary.  See what I mean?<br><br>Getting your graduate degree usually entails being motivated by some other source.  I started my graduate program when I decided to leave my current job.  I wanted that advantage over the next applicant when I found the job I was looking for.  The people in my class were of the same caliber.  Some were just getting the degree “while they were still in school mode”.  Most of the others were like me, going back the 2nd time for actual learning and motivation to find something better than our current jobs.<br><br>But you don’t need an MBA to get a job – right?  True, but it does make it a lot easier finding and holding a job you like and getting compensated for your education.  I took a career path after college for several years.  After a while, I realized I had time before retirement for another full and complete career of something totally different.  Nobody says you have to work at the same place for 35 years.  I know a lot of folks who do.  <br><br>The main thing is – you’ve got time to decide where you want to go and what you want to be.  Me?  I quit that dead end job.  I enjoyed my MBA classes so much I want to be a college instructor.  Don’t think I can handle the doctoral program yet, but in time. . .maybe.  What are your interests?  Find them.  Set goals.  You can do it.<br>
