		<h1 align="left">
				<font size="3">The Bush "There Or Here" Fallacy and the War in Iraq</font></h1></font></strong> 
					<p align="left"> 
					<font face="Arial" color="#000044"><font size="2">By</font><strong style="font-weight: 400"><font size="2"> 
					Christopher Brown</font></strong></font><br>
                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Today we wish to examine a fallacy, or error in reasoning, which we have found springing up now and again in today's popular discourse about the so-called War On Terror. This one comes straight from the top -- well, not the VERY top -- but from Washington D.C. You have heard the President say it on national teevee, and so have we: "We either have to fight them [the terrorists] over there [i.e. Iraq], or we have to fight them over here [i.e. inside the U.S. border]."<br><br>Now we have chosen to examine this particular Bushism because, here, Mr. Bush has offered quite the textbook example of what informal logic-addicts call, a "false disjunction," or simply the "either-or" fallacy. To commit this error in reasoning, you only need to oversimplify a range of many options, reducing it to a pretended range that limits them to two logically-possible options only.<br><br>For instance, isn't possible that, if the U.S. pulled its troops from Iraq, using many of them to assist with border patrol duties, that we could avoid fighting "them" here by not letting them in, and yet not fight them "there" either? Now, to be sure, many will hasten to point out that they see this as impractical, ill-advised (for whatever reason), etc. My only point remains this: the option I have mentioned is logically possible. And I could imagine quite a few others.<br><br>For instance, the U.S. could spend a handsome little sum on policing our domestic internal affairs, and arrest all terrorists before they can do any harm. We have already arrested quite a few of them here without any fight whatever. One might argue that bloodless arrests seem much better, not to mention a good deal cheaper, than national invasions where the whole countryside gets shot up.<br><br>Now, if the U.S. can act with pre-emptive success in Iraq (for the president has suggested many times that it can), why can it not do so also much closer to home? But if the U.S. cannot do so on its home turf, why should anyone think they can do it in Iraq?<br><br>Remember, I do not mean to argue here against the U.S. presence in Iraq, but only to critique one particular reason offered for it by the president. He has, after all, listed quite a few different reasons for the invasion, at different times -- which may or may not be a good thing.<br><br>For today, then, let the reader take away this lesson in the logic of popular discourse -- never reduce a range of many possible options to two only, unless you prepare well enough to show that the others do not represent truly logical options. Otherwise, you will have committed the either-or fallacy.
