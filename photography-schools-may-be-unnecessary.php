		<h1 align="left">
				<font size="3">Photography Schools May Be Unnecessary</font></h1></font></strong> 
					<p align="left"> 
					<font face="Arial" color="#000044"><font size="2">By</font><strong style="font-weight: 400"><font size="2"> 
					Tom Ray</font></strong></font><br>
                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">What I learned from a mentor that enabled me to go from an amateur photographer to a professional portrait photographer in very specific steps is something I like to pass on. Rather than spending countless hours in classes learning every possible detail about photography, I learned just the necessary specifics to get started in the business and now I work out of my home part-time earning a full-time income and have been in business for over 17 years; but I started out with practically nothing, just an interest in photography and the need to earn more money. 
<br> 
<br>One of the things that my mentor taught me was the Three Classic Elements to produce “salable portraits.”
<br> 
<br>"Salable" is an industry term every photographer quickly becomes familiar with to distinguish between the everyday reality of making money versus creating those “artistic competition” or “award winning prints” which don't earn the money.
<br> 
<br>I've been in the business for over 17 years now and I'm still amazed that: 
<br> 
<br>People don't buy the award winning prints that you see wearing many of the ribbons at professional photography conventions. 
<br> 
<br>When my clients are faced with the choice of buying an artistic pose of their child being demure and not looking directly into the camera or buying a pose smiling close-up straight into the camera, they buy the smiling close-up every time.  
<br> 
<br>Not very original, but I'm telling you now so take note:  
<br> 
<br>Happy people whose faces you can readily see are the most salable prints.  
<br> 
<br>They'll never tell you this at a photography workshop, seminar, Annual Convention or at a photography institute because their job is to create award winning photo artists rather than people whom simply make a living, but...
<br>if you haven't learned all the fancy lighting techniques, then you've saved time because the most important thing about light is having enough to keep the face out of the shadows. 
<br> 
<br>People prefer any kind of light, as long as there is enough of it to light the face and eyes so you can get a good look at the person!  
<br> 
<br>The quality of light people prefer for portraits is soft light, whether it be from an artificial source like a flash umbrella or a natural source from the sky at sunset, but other than a soft quality of light they want enough of it to SEE the face of the person you're photographing, even if it is a flat almost straight on technique.  
<br> 
<br>You may not win any competitions or awards this way, but if you get plenty of light on the faces you'll create salable prints.
<br> 
<br>This leads me to talk about fill flash. There are times outdoors when you'll need a flash on your camera to fill in dark shadow areas mostly in the eye sockets.  Just use one f stop less flash than the existing ambient light calls for.  That's enough light to fill the shadows and don't worry about not lugging around a portable umbrella to get the perfect modeling technique.  
<br> 
<br>My mentor is right again: there is no change in the sale.  The customer pays for well lit faces, not perfect modeling.  I've tried it both ways and the customer buys the same amount of pictures in the same sizes no matter what you do.  
<br> 
<br>Element number Two:  Body Positioning.
<br> 
<br>This is a little more detailed area, but it is important, believe me.  
<br> 
<br>My basic education from my mentor began with the same advice I'll pass on to you: 
<br> 
<br>You should rarely photograph anyone straight on. 
<br> 
<br>The exception to this rule will be for family and large groups, which for reasons of body placement will often break this rule.  But for individuals or smaller groups of people this rule applies.
<br> 
<br>Now, when you're not just photographing a head and shoulders close-up you'll have to understand other aspects of body positioning that makes people want to buy their pictures.  Hands. They should always be turned slightly so they are seen from the edge with fingers together, or hide the hands altogether behind your subject or somebody else next to them.  Never position hands straight on with open fingers. 
<br> 
<br>Simply put, anything that minimizes how much hand you see works to make it a better portrait.  This is always more flattering in a portrait and you'll see they are the ones people buy. 
<br> 
<br>Crossing legs at the ankles refines the pose and minimizes this area of the 
<br>body making it more appealing.  
<br> 
<br>Look at it this way, what's less of a distraction: two legs leading to two ankles leading to two feet -- or two legs blending into one ankle section with blended feet?  Surely it's the latter.  
<br> 
<br>When standing, one cannot simply cross their ankles unless they have something to lean against, so I will have one foot in front of the other in such a way that they taper into one general unit. Have them place their weight on the back leg (remember, they are at a slight 3/4 angle) and bring the front leg forward and slightly tilt the foot to face out toward the camera.  
<br> 
<br>Whenever I'd show my mentor my portraits that I was just unsure of, it was these recurring themes that he patiently pointed out to me.  
<br> 
<br>As I began to look for these simple things during my portrait sessions,
<br>my pictures got better!
<br> 
<br>I can't stress enough how basic, but important, it is to watch for these details.  
<br> 
<br>I have people come to me who went to the contract photographer for their High School Senior yearbook portrait and disliked their picture.  They want me to take one that they can proudly give out to friends and family.  Usually the problem with the pictures I've seen is that the photography school graduate “intern” who works for the contract photographer took the photo without paying attention to some minor detail.  I get it right and my reputation grows from “fixing” the contract photographer's mistake.  
<br> 
<br>The techniques for salable body positioning are what you look for in any pose you try whether close-up or full body. 
<br> 
<br>When photographing people full body standing, seated or reclining on the ground, noticing body angle, hands and feet is the way to “fine tune” your portrait and distinguish it from just a “snapshot”. 
<br> 
<br>Lastly, I must share my favorite body positioning tool that makes it so easy to make a better portrait than someone who doesn't really know what they're doing: the head tilt.  
<br> 
<br>A woman alone tilts her head just slightly in either direction to make a more stunning portrait.  A man's head can stay straight up or tilt slightly away in the opposite direction from his most forward shoulder but never back towards his most forward shoulder.  
<br> 
<br>Element number Three:  Salable Composition
<br> 
<br>There are many compositional techniques in many books, but it doesn't take all that knowledge to make portrait compositions that are what the typical consumer considers good enough to call professional.  
<br> 
<br>Once you know what the consumer considers salable, you will be able to reproduce it again and again for other clients. You also will thank me for saving you from thinking that in order to be good enough to sell portrait photography you have to create grand artistic images.  You just have to know what works and be able to repeat it for the friends of your clients whom will be getting your business cards by way of referral.  
<br> 
<br>When photographing one individual person, it's so simple I don't think you need too much input for that.  In fact, I believe you know the naive simplicity with which you thought “hey, I can do this for a living” after taking some portraits of a friend or family member.  Yet it truly gets challenging when there is more than one person involved.  
<br> 
<br>I know of a local professional who has referred family portrait clients to me as she specializes in children outdoors. Do you know what that really means?  It means she's intimidated by having to do groupings, but that's okay, most people are.  
<br> 
<br>So here's the rule of salable composition: 
<br> 
<br>Keep everybody's head at a different level.
<br> 
<br>Like I told you, I didn't have a fancy College degree so my mentor had to keep it simple enough for me.  In some cases, you will recognize that it's not possible, but if you do your best to stagger head height from individual to individual, you will be creating professional looking images.  
<br> 
<br>You will stand some people, seat some in chairs, seat some on the arms of chairs, seat some on the floor, kneel some, crouch some, lay some down, but you will achieve staggered head heights and salable compositions.  
<br> 
<br>Tip heads inward toward one another for unity when photographing a family group.
<br> 
<br>Note that men are usually positioned higher than women.  
<br> 
<br>No, I'm not aware of being a chauvinist pig, but I am aware that this is what usually sells.  Not the images where mom's higher than dad but where dad (even if he's actually shorter!) is positioned just a head or so above mom. 
<br> 
<br>Once you understand the rules, you can bend them where you need to in order to make a portrait work; but people will see that you know what you're doing as you position them for a good composition and especially when they see your finished work.    
<br> 
<br>My mentor critiqued my work time and time again over several years as I brought images and questions to him.  It almost always boiled down to my understanding these most simple aspects that I've shared with you.  
<br> 
<br>I know it's not customary to learn photography on such simplistic terms, but trust me; I've had exposure over the years to many different photography educational venues such as classes, workshops, conventions, guest speakers, lectures, teaching videos and books but never have any of the teachers been willing to simply say “look, there are just a few rules to follow and people will be happy with their pictures”.  Never have I received more helpful advice than I received from my mentor. This is the reason I've written "Professional Photography: Success Without School."  Not because schools have no place in the lives of photographer hopefuls, it's just that they are not the only way to guarantee success in the business.   
<br> 
<br>I guess if I could sum up the philosophy my mentor embodied in word form I'd say it was rather like this: 
<br> 
<br>“Not everybody wants a masterpiece. Most people just want to remember their loved ones as happy.  It's not hard to capture that with your camera, just don't stand them in hard sunlight, standing in a straight line facing straight toward the camera.”
