                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Making the decision to continue your education online is an important one.  It is vital, however, that you do your research to make get the most out of your education.  Here are some tips to help get you started.<br><br>The first thing you should look at when deciding on your school is their accreditation.  Many diploma mills will claim to be accredited.  However, upon further inspection, you will find that many are not or are accredited by a company, which has been set up by the school itself, to lend legitimacy to its programs.  Be diligent in contacting the college and asking for proof of their accreditation.  Don't be afraid to get in touch with the awarding body to verify that what the college is telling is in fact true.  Many diploma mills do not expect prospective students to conduct due diligence on them, so this can be to your advantage.  The U.S. Department of Education maintains a website which lists recognized awarding bodies and the Distance Education Council will be happy to help you verify a particular college's accreditation.  <br><br>Read the course catalogue closely to get a sense of what sort of programs they offer.  In the rush to capitalize on the explosion in the online education market, many colleges will offer degrees in areas before than can develop a reputable or experience faculty.  Does the instructor actually have professional and academic experience in the field that they teach? If you have any questions or queries, contact the university or the instructor and ask them.  This should help you get a better sense of what the institution and its instructors are like.<br><br>One of the best indications that an online school is a diploma mill is if their admissions policies are too lenient or non-existent.  While there are many good colleges with relaxed admissions procedures, diploma mills are often simply interested in taking money off of students in the form of tuition or fees.  If the college is too keen to grant you credits based upon past courses or life-experience without careful consideration, than that is usually a sign that the school is best avoided.<br><br>While earning your degree online can be a rewarding experience, it is important that you know what to look for when choosing your school.<br>
