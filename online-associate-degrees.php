rsity is accredited by the Higher Learning Commission to grant bachelor and associate degrees online. They offer online associate degree programs in Applied Management, Business Administration and Information Technology as well as an Associate of Science Degree in General Studies.<br><br>Crown College offers an online education that combines the traditional arts, sciences and humanities with the professional disciplines related to these fields. Their online Associate of Science degree in Criminal Justice program provides the graduate with the capacity to compete for jobs in Law Enforcement, Community Corrections, Prison/Jail Officer, Private Investigations/Security. Their online Associate of Science in Paralegal Studies degree provides the graduate with the skills to compete for jobs as a Paralegal, Court Clerk, or Judicial Assistant.
