				<span style="font-size: 11pt">Twenty years ago a typical college classroom had very little technology.  Maybe there were a few computers, but for the most part students turned in assignments written from a typewriter.  Ten years ago technology began to seep into the college academic setting.  For example, Professors started simple WebPages containing course information and perhaps links to reference resources.  <br><br>In the late 1990’s, however, college academia took a giant step forward in elearning by introducing courses that could be taken entirely online.  The environmental trends in elearning have changed dramatically from the infrequent use of the Internet and elearning to relying on the elearning for an entire course.<br><br>This environmental trend in elearning opened up the opportunity to study at the college level to many more students.  Technical colleges, for example, are home to many nontraditional older students.  These students have full time jobs, families, and little free time.  It is difficult to miss work to attend class during the day.  Night classes are possible, but that means less time with family.  That is when an online course can save the day and allow these types of students to work on their degree.<br><br>Online courses are available at many four-year and two-year colleges and universities.  In the last few years, online colleges have emerged that offer an entire degree program online.  These comprehensive courses of study allow a more diverse student population to have access to the education they seek.<br><br>Another environmental trend in elearning is the way classes are conducted.  Classes are administered through software programs such as Blackboard and WebCT.  These web-based programs allow students to login to a virtual classroom.  They can access course materials such as lecture notes, assignments, and tests.  They can communicate with the instructor and other classmates through email messages and a class message forum.  <br><br>Professors, teachers, and instructors also have an array of options.  There are calendar, email, and syllabus tools.  Electronic grade books are a nice feature of elearning software programs.  Electronic grade books are more than just a repository of grades.  They can actually calculate final grades based on specific classroom grading criteria and policies.  This is a welcome addition for many teachers that are technologically savvy.  Plus students can view their grades online throughout the semester and track their progress.<br><br>As technology progresses so will the elearning environment.  The academic options are far different from what they were even ten years ago.  Imagine what the options will entail ten years from now.
