		<h1 align="left">
				<font size="3">A short biography on some of Europe's most loved and hated Monarchs -  Pt 3 King Ludwig II</font></h1></font></strong> 
					<p align="left"> 
					<font face="Arial" color="#000044"><font size="2">By</font><strong style="font-weight: 400"><font size="2"> 
					Stuart Bazga</font></strong></font><br>
                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">During the last thousand years, European Monarchs have ruled Europe and the world with an iron fist and by fear, compassion and hatred. As their wealth grew from the riches of newly conquered continents and lands, they began building some of the worlds greatest castles as a sign of their status and wealth, leaving behind a legacy of beauty and splendor that has lasted well into the 21st century. 
<br>
<br>These members of royalty have included tyrants, the mentally insane, drunks and the psychotic, who have murdered for pleasure and raped for enjoyment. Imposed terrible taxes on already poor citizens. They married for financial power and traded in lives. Kinfolk were murdered so a favorable son could rise through the ranks. As their power increased so did the atrocities, bringing with it the hatred of a nation and it’s people.
<br>
<br>Join me as we take a trip back in time, discovering which Monarchs were tyrants, mentally unstable, drunks and psychotic, as well as those who were loved by their people.
<br>
<br>This series of articels will highlight the lives of Vlad Tepes(Dracula), King Ludwig II and King George III to name a few.
<br>
<br>
<br>King Ludwig II of Bavaria, named after his grandfather, was born in Nymphenburg Castle outside Munich on August 25, 1845 and was the eldest son of King Maximillian II and Queen Marie. As a boy, Ludwig’s favourite time of the year were the summer holidays spent at the Royal Castle Hohenschwangau which his father restored between 1832 and 1836. 
<br>
<br>
<br>In 1858, at the age of thirteen, Ludwig was introduced to Wagner’s opera - Lohengrin, the story of which centres around the heroic medieval Swan-knight Lohengrin, by his governess. The young Ludwig instantly fell in love with its concept and it was the begining of a life long love with all of Wagner’s works. It was not long before he was acquiring and reading every book written by Wagner. On February 2nd, 1861, Ludwig attended his first Wagner opera - Lohengrin, the Knight of the Swan which left a lasting impression on the young Prince. 
<br>
<br>
<br>In March of 1864, his father died at the age of 53 and Ludwig, at the age of eighteen, became King Ludwig II. With in days of coming to the throne a young and impressionable King ordered his officials to seek out Wagner and bring him back to Munich, taking it upon himself to become Wagner's patron. He settled his debts, and set him up comfortably in an Italianate-style villa. 
<br>
<br>
<br>Munich society began to grow weary of Wagner's arrogance and were also increasingly jealous of his hold over their young King. Eighteen months after his arrival, Wagner left Munich for Switzerland and Ludwig fled to Hohenschwangau. The one person that brought joy into his life had been taken from him. 
<br>
<br>
<br>In 1866 war broke out between Austria and Prussia in what became known as the Seven Weeks War. Because of her strong links with Austria, Bavaria was drawn into the conflict on the Austrian side. In a secret treaty King Ludwig II placed the Bavarian army at the disposal of the Prussian General Staff. 
<br>
<br>
<br>Ludwig was to marry in August of 1867 but he was unhappy with the relationship so changed the date to October 12th, which incidentally was the date that both his grandfather and father married. Ludwig was very unsettled at the prospect of marrying and as such voiced his apprehension to his Court Secretary, confessing that he would rather drown himself than marry. He wrote to Wagner "Oh, if only I could be carried on a magic carpet to you . . . at dear, peaceful Tribschen (Lucerne, Switzerland.) - Even for an hour or two. 
<br>
<br>
<br>In November, King Ludwig II broke of his engagement fleeing to his beloved Alps. He wrote to Wagner from Hohenschwangau on 21 November, 1867; "I write these lines sitting in my cosy gothic bow-window, by the light of my lonely lamp, while outside the blizzard rages. It is so peaceful here; this silence is stimulating, whereas in the clamour of the world I feel absolutely miserable. "Thank God I am alone at last. My mother is far away, as is my former bride, who would have made me unspeakably unhappy. Before me stands a bust of the one, true Friend whom I shall love until death. . . If only I had the opportunity to die for you." 
<br>
<br>It was at this point that Ludwig began to plan and build his castles. The task of being king was becoming a heavy burden. He had at the age of only 20, sent thousands of his countrymen to fight in the Seven Weeks War. Two years after his failed romance, Ludwig again had to send his countries men into battle. This time it was against the French in what became the Franco-Prussian War. From then on Ludwig withdrew into himself and into a world of make-believe. The plans for both  Neuschwanstein and Linderhof originated from this time in his life (1869). 
<br>
<br>
<br>Ludwig was a changed person. He went from a slender youth to a huge man in just a few years, spending all his time in the mountains at Hohenschwangau and Linderhof as well as his small mock-Gothic castle at Berg, beside Lake Starnberg. He refused any contact with his ministerial staff and sought only the companionship of the mountain people. The only time King Ludwig II ventured out of the mountains and into Munich was at the annual investiture and banquet given in the Residenz for the Knights of the Order of St. George, Bavaria's highest Order of Chivalry which Ludwig was the Grand Master. 
<br>
<br>
<br>Shortly after Prussia's victory in the Franco-Prussian War, Bismark sought Ludwig's approval for Bavaria to enter a unified German Empire with Prussia as leader. After several days, Ludwig succumbed and wrote a letter inviting Wilhelm II to become Emperor of a united Germany. King Ludwig II handed over his beloved Bavaria, becoming a lonely figurehead in a constitutional monarchy. Life had dealt Ludwig blow after blow and it was these events that were the root of his seclusion and alleged “madness”. With his world falling apart around him King Ludwig II withdrew from it into a world of his own making. 
