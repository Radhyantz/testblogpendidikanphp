                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Join me as we take a trip back in time, discovering which Monarchs were tyrants, mentally unstable, drunks and psychotic, as well as those who were loved by their people.
<br>
<br>
<br>King George III who suffered from porphyria, a maddening disease, was born in 1738 to Frederick, Prince of Wales and Augusta. In 1761 George married Charlotte of Mecklinburg-Strelitz and together produced fifteen children: nine sons and six daughters. 
<br>
<br>King George III came to the throne in 1760 and was determined to recover the power lost to the ministerial council by the first two Georges by systematically weakening the Whig party through bribery, coercion and patronage. Prime Minister, William Pitt the Elder was toppled by Whigs in 1763 after the signing of the Peace of Paris, and men of ordinary aptitude were then hand-picked by George as Cabinet members to become little more than yes-men. Bouts with madness and the handling of the American Revolution eroded his support and the power of the Crown was granted again to the Prime Minister. 
<br>
<br>In 1763 The Peace of Paris brought an end to the Seven Years' War with France and Great Britain emerged from the conflict as the world's greatest colonial power. England thrived but King George III's ongoing commitment to taxing the American colonies to pay for military protection led to conflict in 1775. The colonists declared their independence from England in 1776, but George stubbornly continued with the war until the final American victory at Yorktown in 1781. The signing of The Peace of Versailles in 1783 ensured British recognition of the United States of America. The stress of these events took their toll on George: his sanity was stretched to the breaking point and his political power decreased when William Pitt the Younger became Prime Minister in 1783. George clawed back some of his powers, driving Pitt from office during the years 1801 to 1804, but his condition worsened and he ceased to rule in 1811. 
<br>
<br>Ten years after the ending of war with France, England joined a continental coalition against French revolutionary forces who sought total French supremacy throughout Europe. By 1797, most of Europe was under French control, with England going at it alone against the oppressive French Republic. The British Navy again proved its worth by defeating French forces at Camperdown, Cape St. Vincent and the Battle of the Nile in 1797, and finally at Copenhagen in 1801. France sued for peace in 1802. 
<br>
<br>Napoleon Bonaparte came to power and in 1803 renewed attacks against England, which lasted until 1814. Arthur Wellesley, Duke of Wellington, led the army whilst Lord Horatio Nelson, who won the decisive battle off Cape Trafalgar, commanded the British navy. In addition to the war with France, England was also at war again with the United States during the period 1812-14, over the British practice of conscripting American seamen into service in the British Navy. 
<br>
<br>In 1814, both wars came to an end; Napoleon was defeated and England agreed not to press into service anymore-American sailors. 
<br>
<br>George's madness ultimately left the fate of the crown in the hands of his eldest son George, Prince Regent. Prince George was put in the unenviable position of attempting to govern according to the increasingly erratic behaviour of his father. 
<br>
<br>King George III died blind, deaf and mad at Windsor Castle on January 29, 1820. 
