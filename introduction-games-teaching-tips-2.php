                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">What? Already?! Teachers are busy preparing for the new school year. One way to set a positive tone in the classroom is to make sure all the children know one another. Here are a few introduction games. (NOTE: Any group of people who need to make introductions can play these games.) Have fun!<br><br>1. While students are sitting on desktops or standing in a circle, the teacher tosses a ball to a student who then introduces himself and tells his favorite game or activity. He then tosses the ball to another student who repeats the procedure. Make sure each child has a turn to introduce himself. Try playing a few minutes each day so children can also name their favorite food, place to visit, musical group, TV show...whatever you want.<br><br>2. While children are sitting in a circle in a darkened classroom, the player in the middle spins a flashlight on the floor. (If children are standing in a circle, the player in the middle spins around while holding the flashlight facing outwards.) The child who is "in the spotlight" when the flashlight stops must introduce himself and tell something else about himself. That person then becomes the next spinner. Give children ideas of what to tell about themselves...favorite movie, best quality, favorite famous person, favorite school subject, and so on.<br><br>3. Have children fold a piece of paper into a certain number of squares. They must then go get signatures from their new classmates, who must introduce themselves as well. Each signature should be clearly written in order to count. The first child to fill his sheet is the winner. If you want to add a little physical activity, have children who are finished walk around the room several times and then take their seats.<br><br>4. For this game, use several hoops or large, circular pieces of butcher paper. This is an innovation of "Musical Chairs" so have a music source available! When the music stops, children who wind up in the same hoop introduce themselves. Instead of walking while the music plays, perhaps your children could hop, jump, skip, etc. As hoops are removed in each round, have the children in that hoop tell something about themselves.<br><br>5. Let your children form groups of five to ten. At the beginning, each child announces his name, clearly, to the group. Then let the group decide how a ball will be passed...rolled, bounced, tossed, rolled around back and then tossed, etc. A player is to say his own name, as well as his new classmate's name, when he releases the ball. For instance, "My name is Bill and yours is Anna." A variation is to have the entire group call out the name of the person who catches the ball. Add details when the names have been learned...favorite pet, birthday, etc.<br><br>I hope these ideas have been useful and have inspired your own creativity. Have a wonderful, rewarding year!<br><br>And remember...Reading is FUNdamental!
