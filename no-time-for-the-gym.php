				<span style="font-size: 11pt">   Dorm Room Exercises Combat College Weight Gain<br><br>	As you have undoubtedly heard, college students around the country are gaining weight at a rapid rate. A recent study by Cornell University found that on average, college freshman gain about 0.5 pounds a week, which is almost 11 times more than the average weight gain among 17-and 18-year olds and almost 20 times more than the average weight gain among American adults. <br>Exercise is a great way to ward off extra pounds, but what college student really has time to visit a gym multiple times each week? Hectic, stressful schedules are almost always partially to blame for the extra weight gain in the first place! A great solution is to find exercises that you can perform right in your dorm room. <br>Try out the following Dorm Room Exercise Routine right in your own room! Please remember to consult your physician before beginning an exercise program. <br>Lunges<br>Equipment: Optional Dumbbells or Barbell – This can be done simply with bodyweight.<br><br>Starting Position: Stand with your feet together and a barbell across your shoulders, or, if using bodyweight, hands on your hips. Take a large step forward and bend your knees down into a lunge position. <br><br>Movement: Exhale as you press yourself back up to a standing position, or continue through with your step into another lunge.<br><br>Push Ups<br>Equipment: Optional Exercise Matt <br><br>Starting Position Beginner: Get on all fours on the matt with your hands directly under your shoulders. Walk your knees back until your upper body is stretched out and your back is flat. <br>Starting Position Advanced: From the all fours position, walk your feet back until your body is stretched out and your back is flat. <br><br>Movement: Inhale as you slowly lower your body down towards the matt. Be sure to keep your back straight and to not allow it to sag in the middle. Exhale as you press yourself back up to the starting position. <br><br>Crunches<br>Equipment: Optional Exercise matt<br><br>Starting Position: Lie on your back on the matt with your knees bent and feet flat on the floor. Support your head by placing your hands behind it, and keep your eyes turned up towards the ceiling throughout the entire movement. <br><br>Movement: Exhale as you raise your upper body up a few inches off of the matt by contracting your abs. Be sure to keep your back straight and avoid pulling yourself up with your hands. Hold yourself in this contracted position for a moment before slowly lowering yourself back down to the starting position.	<br><br>	Couple the above exercises with regular walks or jogs around campus, and you will be well on your way to combating college weight gain. For more information on dorm room exercises and avoiding college weight gain visit www.AvoidTheFreshman15.com. You don’t have to be stuck with unwanted weight gain just because you are a busy college student! <br><br>
