				<span style="font-size: 11pt">Like most Americans you may be skeptical of why an unseen online agency would want to “help” you by checking your credit score for free.  I mean, aren't they just out there to get your money?  The truth is, these credit checks can really help you, especially if you are trying to improve your credit. <br><br>First, by checking your credit online, you avoid “dings,” which lower your score.  Many consumers are afraid to investigate their credit because they think their score might drop, putting them in even worse shape. This isn't true with online credit reports. Thus, without dinging your credit, online sources allow you to check out your score, for better or worse. <br><br>Alternately, if you have been denied credit within the last 30 days, you can ask the credit bureau for a free report that won't ding your credit. You do have to use a local reporting agency, which can take more time than using an online source. Most people go in person to the agency to request the report and then pick it up.  Both of these steps seem quite tedious when compared to online credit checks.<br><br>Another benefit of online credit reports is the ability to see both good and bad credit you have on your report from the privacy of your own personal computer.  No nosy bankers trying to get your a credit card or auto loan. Without sales pressure, you are less likely to get into more debt and lower your credit score.<br><br>Lastly, you can verify the accuracy of your credit.  The online report will be taken from each of the three national credit bureaus. Occasionally you will see accounts that you dispute, which gives you the opportunity to contact the bureau.  To do this, you must write a letter explaining exactly what is incorrect or disputed.  The agency must investigate.  When they contact the creditor, he has 14 days to respond or the item is deleted off your credit.  A little known secret is that if the item is less than $500 or older than one year, most creditors won't bother responding, whether your dispute is accurate or not.  Disputing bad credit is the most direct way you can improve your credit.  When your bad credit is reversed your credit score will increase. Most importantly, after your score goes up you have the right to demand that your updated credit report be sent to all those who have denied you credit in the last six months. Good news!<br><br>So, if you wonder how good, or bad, your credit score is, don't be afraid to check it out online.  The report is excellent information that arms you with power to change and improve your credit.  Most people with bad credit don't know much about how it gets that way or how to fix it.  Your knowing both is a good start to improving your credit.
