				<span style="font-size: 11pt">You're thinking about furthering your career or finally getting your college degree.  But with so many educational choices online, how do you know whether it's right for you?  Here are some pointers you should consider, before making your decision.<br><br>There are many perks associated with online study.  Unlike traditional learning, distance learning can offer you greater flexibility, as you don't have to waste time- and money- commuting to the campus and you can choose a program that truly suits your interests or professional needs, because you are not confined to the classes that are offered locally.  <br><br>Another advantage to distance learning is the fact that you can continue to work, because you don't have to worry about fitting your classes around your job schedule.  This is a distinct advantage over traditional methods of learning, because this option allows you to continue earning while you study.  Online programs often cost less than their traditional counterparts, so you can save money on tuition, if you pursue your degree through an online university.  <br><br>Online study is also a great option for those students with physical handicaps, which may prevent them from travelling to a traditional campus.  Likewise, if you are shy, or perhaps even language-challenged, participating in an online course may be the perfect alternative to a live class.<br><br>There are some drawbacks to online education, however.  For example, traditional instructors may not successfully make the transition from the classroom to the online platform and it can sometimes be difficult for them to transfer course content or offer additional support to the students.    <br><br>While the technological aspects of online learning can make the courses more interesting and interactive, some students may not have the computer hardware to support the specific programs required for the class.  Many courses require DSL or a high-speed internet connection in order for the student to interact effectively with the instructor or other students.  Similarly, online learning can rob the student of the conventional aspects of classroom interaction, such as being able to talk to the instructor or the other students, face-to-face.<br><br>In spite of the drawbacks, however, many new and returning students are opting to study via distance learning and are finding it to be a very rewarding experience. <br>
