				<span style="font-size: 11pt">You have chosen your university to attend in your distance learning program and are excited about being a student again for the first time in a long time. Relax, breathe and just follow the process to get your online degree.<br><br>Registration<br>Registering for a course online is a lot easier than commuting to the school and wandering through the maze of confused students trying to make their schedule fit their lives. You just have to make it through screen after screen of information. If you get stuck, look for help pages or FAQ (frequently asked questions) sections to seek your answer.<br><br>Your First Course<br>Whether you are taking a course online or in person it may feel uncomfortable “sitting” in your first online class. It’s okay to feel a little strange. This is a new experience for you and you have to get used to learning this way if you are going to complete your online degree. Pay attention and stay focused and you’ll be fine. Feel free to ask for help from your instructor if you don’t understand something.<br><br>Doing Your Homework<br>For every hour spent online at your virtual class you will have to devote at least five hours of work to study and complete your homework and assignments. That is a good chunk of time and you have to re-organize your life and your family responsibilities to do that. But most family members and friends are quite respectful of your goal to get an online degree and will be happy to accommodate you. Look for a quiet time and space to study and get to work.<br><br>Check Your Progress<br>If you are moving at the same speed through your course as your instructor and you feel comfortable with the basic concepts that they have presented then you are probably doing okay. If you are completing all of your offline reading and assignments on time then that’s a good sign as well. If you notice that you are slipping a little then put a little extra effort in.<br><br>If you find that you are slipping a lot then ask for help. The instructor will be glad to assist you by helping you to understand some of the basic principles of the course, but remember you have to do the work.<br><br>Finish Strong<br>Like the anchor of a relay team you can always put in a little push of extra study near the end of a course to try and improve your finish. Before you know it you will have successfully completed your first course and are looking forward to the next one to help achieve your online degree.
