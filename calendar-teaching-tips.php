                  <br>
                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">Stop! Don't throw away those old calendars! There is so much you can do with a calendar to reinforce academic skills. Here are a few suggestions:<br><br>1. Writing - Use the pictures as story starters by having children write an original story, poem, or song centered around them.<br><br>2. Art - Cut out the pictures and use them to make your own greeting cards. Write original sayings to go inside.<br><br>3. Classification - Have a selection of different kinds of pictures available so your children can sort them according to general similarities...all pets, all cars, all buildings, etc. Older children can sort them into more specific categories such as sports cars, SUV's, etc. <br><br>4. Flash Cards - Make your own by putting a picture on one side and writing the word on the other. For older children, write a sentence including the word. Have children make their own flash cards! (NOTE: Cut out the numbers to make math flash cards.)<br><br>5. Left-to-Right Progression - As you teach young children the days of the week, always point out that you are starting from the left and going to the right.<br><br>6. Days and Dates - Ask questions that review days, dates, and ordinal numbers. For instance, which day of the week is the third? What is the date of the fourth Wednesday? When does Autumn begin (day and date)? Which holiday is on September 5?<br><br>7. Math - Ask questions to review before and after. For instance, what will the date be five days after Labor Day? What will that day be? If National Farm Animals Awareness Week is September 18, what was the date nine days earlier? Which day of the week was it? What would the sum be if you add the dates of all Tuesdays in the month? For older children, make it a 2-part problem and have them subtract from, multiply, or divide the answer. Using the dates of the calendar, what is the difference of the last Friday and the second Friday? Are you multiplying, yet? Skip color all the boxes in multiples of 8, for instance. You would then have boxes numbered 8, 16, and 24 filled in. Since you will have several days left over, children can learn the term <br>'remainders' and get ready for division!<br><br>8. Reading - Alphabetize all the holidays and special days. How many syllables are in each name? List the holidays, then the special days. Which countries are represented? Do some research and find out more about unfamiliar days.<br><br>I hope these ideas have been useful and have ignited your own creativity.<br><br>And remember...Reading is FUNdamental!<br>
