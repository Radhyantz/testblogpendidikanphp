                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">In British Columbia, Canada, salmon purse seiners line up at fishing access points, forming well defined queues. These queues were measured over time, using a one-dimensional recording scale. Sixty-one overflights of Johnstone Strait and Queen Charlotte Strait were attempted; 51 flights were completed.<br><br>Two models were presented for exploitation rates in relation to queuing patterns. The overflight model was fit to the line-up distributions. One underlying assumption was that the skippers possessed fairly accurate information regarding the distribution of catches (analysis of variance methods utilizing skippers' logbook data showed that line-up lengths reflected catch rates). The model fit well and the parameter estimates reflected anecdotal and statistical information about fish behavior. The exploitation rates saturated at an effort level of 100 vessels (whereas the maximum effort observed was 363 boats) and indicated that (at saturation) the fleet caught 80% to 90% of the vulnerable migrating salmon present in Johnstone and Queen Charlotte Straits during what were commonly 48- or 72-hour fishing openings. (Note: Salmon successfully migrating through the strait on days that were closed to seiners and salmon that were not vulnerable to the gear--e.g., below the depth of the nets--escaped the purse-seine fleet.)<br><br>In general, traditional assumptions were rejected. Vessels did not operate independently. Boats were not distributed in a random fashion. The overflight model provided predicted exploitation rates. The exploitation response to effort was qualitatively distinct from the forms incorporated in traditional models.<br><br>In partial summary, the question is one of fishing power--the ability of gear, boats, or fleets, in the B.C. and P.E.I. cases and others, to exploit or overexploit fish stocks. Without a historical perspective based on quantitative (and innovative) field research, we are doomed to repeat our w