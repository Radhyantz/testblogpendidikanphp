				<font size="3">A short biography on some of Europe's most loved and hated Monarchs -  Pt 4 Queen Mary I</font></h1></font></strong> 
					<p align="left"> 
					<font face="Arial" color="#000044"><font size="2">By</font><strong style="font-weight: 400"><font size="2"> 
