                  <font face="Arial">
                  <strong style="font-weight: 400">
				<span style="font-size: 11pt">During the last thousand years, European Monarchs have ruled Europe and the world with an iron fist and by fear, compassion and hatred. As their wealth grew from the riches of newly conquered continents and lands, they began building some of the worlds greatest castles as a sign of their status and wealth, leaving behind a legacy of beauty and splendor that has lasted well into the 21st century.
<br>
<br>These members of royalty have included tyrants, the mentally insane, drunks and the psychotic, who have murdered for pleasure and raped for enjoyment.  They have imposed terrible taxes on already poor citizens. They married for financial power and traded in lives. Kinfolk were murdered so a favorable son could rise through the ranks. As their power increased so did the atrocities, bringing with it the hatred of a nation and it’s people.
<br>
<br>Join me as we take a trip back in time, discovering which Monarchs were tyrants, mentally unstable, drunks and psychotic, as well as those who were loved by their people.
<br>
<br>This series of articles will highlight the lives of Vlad Tepes (Dracula), King Ludwig II and King George III to name a few.
<br>
<br>Queen Mary I of England was born in 1516 to Henry VIII and Catherine of Aragon and was the first British monarch to rule in her own right. Mary was pronounced queen in 1553 and ruled for five years after the death of Edward VI.    Mary was well educated and learned to speak Latin, Spanish, French and Italian.  She was also taught Greek, science and music.
<br>
<br>1547 saw the death of her father and her half brother Edward VI crowned king. Edward was England's first Protestant monarch; his Parliament's Act of Uniformity prescribed Protestant rites for church services.  Mary, wanting to keep her Roman Catholic faith, asked to be allowed to worship in private in her own chapel.  Upon being ordered to discontinue this practice, Mary appealed to her cousin, the Emperor Charles V.  Charles subsequently threatened to declare war against England if Mary's religious rights were infringed.   Mary was never bothered again and was left to worship in private.
<br>
<br>Edward died in 1553 whilst Mary was staying at Framlingham Castle in Suffolk. He had no wish for the Crown to go to either Mary or her half sister Elizabeth, so had them both excluded from the line of succession in his will.
<br>
<br>One of Mary’s first acts after came to power, was to bring the Catholic faith back to England by initially scrapping the religious proclamations of her half brother, Edward VI. Mary replaced the proclamations with the old English laws. Heresy against the church was now punishable by death. The reintroduction of this act earned Mary the nickname, “Bloody Mary”. During her short, five-year reign, Queen Mary I had more than 300 subjects burnt at the stake for the act of heresy. The most notable of these was the Archbishop of Canterbury, Thomas Cranmer.<br>
 
<br>In 1555, in an effort to produce a male heir, Mary married prince Philip II of Spain. This did not go down well with the people, as many viewed Spain as an enemy of England. Twice during her rule, Mary thought she was pregnant with child, displaying all the symptoms. Alas, this was not so as her symptoms were a sign of a false pregnancy. Mary had convinced herself that she was pregnant and the body reacted accordingly. 
<br>
<br>Following the advice of her husband, Mary allied herself with Spain during the war against France. The subsequent consequences of her actions were that England lost her only and last remaining foothold in the country – Calais. Sadly, in 1558, Philip II left her and went back to Spain to claim the Spanish throne. 
<br>
<br>Queen Mary I, childless and without a husband was forced to recognize her sister, Elizabeth, an Anglican Protestant, as the next ruler of England. Although Mary tried to persuade her sister to convert and accept the Roman Catholic faith, Elizabeth refused and went on to become Queen Elizabeth I.
<br> 
<br>England suffered under the leadership of Mary: the economy was in ruin, religious dissent reached its pinnacle and England lost her last foothold in Europe. Jane Austen wrote about Mary: "This woman had the good luck of being advanced to the throne of England, in spite of the superior pretensions, Merit and Beauty of her Cousins Mary Queen of Scotland and Jane Grey. Nor can I pity the Kingdom for the misfortunes they experienced during her reign, since they fully deserved them..." 
<br>
<br>Mary died at the age of forty-two of influenza, uterine cancer or ovarian cancer at St. James's Palace on 17 November 1558 and is buried in Westminster Abbey beside Elizabeth. The Latin inscription on their tomb translates to "Partners both in Throne and grave, here rest we two sisters, Elizabeth and Mary, in the hope of one resurrection".
